/**
 * 转义可能破坏提示词结构的特殊字符
 * @param content 原始内容
 * @returns 转义后的安全内容
 */
function escapePromptContent(content: string): string {
  return content
    // 转义反引号，防止破坏JSON结构
    .replace(/```/g, '´´´')
    // 转义可能的JSON结束符
    .replace(/}\s*$/gm, '} ')
    // 保留其他内容不变
};

